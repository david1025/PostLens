  void _saveSettings() {
    final req = ref.read(requestProvider);
    final s = Map<String, dynamic>.from(req.settings);
    s['socketSettings'] = {
      'verifyCertificate': _verifyCertificate,
      'clientVersion': _clientVersion,
      'handshakePath': _handshakePathController.text,
      'timeout': int.tryParse(_timeoutController.text) ?? 0,
      'reconnectAttempts': int.tryParse(_reconnectAttemptsController.text) ?? 0,
      'reconnectInterval':
          int.tryParse(_reconnectIntervalController.text) ?? 5000,
    };
    s['socketEvents'] = _events;
    s['socketArgs'] = _args;
    s['socketArgType'] = _argType;
    s['socketAck'] = _ack;
    s['socketEventName'] = _eventNameController.text;
    ref.read(requestProvider.notifier).updateAllSettings(s);
  }

  void _toggleConnection() {
    final t = ref.read(translationsProvider);
    if (_isConnected) {
      _socket?.disconnect();
      setState(() {
        _isConnected = false;
        _messages.add(SocketMessage(
            t['disconnected'] ?? 'Disconnected', false, DateTime.now()));
      });
    } else {
      try {
        final url = _urlController.text;

        final opts = IO.OptionBuilder()
            .setTransports(['websocket'])
            .disableAutoConnect()
            .setPath(_handshakePathController.text.isNotEmpty
                ? _handshakePathController.text
                : '/socket.io');

        final timeout = int.tryParse(_timeoutController.text) ?? 0;
        if (timeout > 0) opts.setTimeout(timeout);

        final attempts = int.tryParse(_reconnectAttemptsController.text) ?? 0;
        opts.setReconnectionAttempts(attempts);

        final interval =
            int.tryParse(_reconnectIntervalController.text) ?? 5000;
        opts.setReconnectionDelay(interval);

        _socket = IO.io(url, opts.build());

        _socket!.onConnect((_) {
          if (mounted) {
            setState(() {
              _isConnected = true;
              _messages.add(SocketMessage(
                  '${t['connected_to'] ?? 'Connected to'} $url',
                  false,
                  DateTime.now()));
            });
            _scrollToBottom();
          }
        });

        _socket!.onDisconnect((_) {
          if (mounted) {
            setState(() {
              _isConnected = false;
              _messages.add(SocketMessage(
                  t['disconnected_from'] ?? 'Disconnected from server',
                  false,
                  DateTime.now()));
            });
            _scrollToBottom();
          }
        });

        _socket!.onConnectError((err) {
          if (mounted) {
            setState(() {
              _messages.add(SocketMessage(
                  '${t['connect_error'] ?? 'Connect Error'}: $err',
                  false,
                  DateTime.now()));
            });
            _scrollToBottom();
          }
        });

        _socket!.onError((err) {
          if (mounted) {
            setState(() {
              _messages.add(SocketMessage(
                  '${t['connect_error'] ?? 'Error'}: $err',
                  false,
                  DateTime.now()));
            });
            _scrollToBottom();
          }
        });

        // Listen to custom events
        for (var ev in _events) {
          if (ev['listen'] == true && ev['name']?.isNotEmpty == true) {
            _socket!.on(ev['name'], (data) {
              if (mounted) {
                setState(() {
                  _messages.add(SocketMessage(
                      data.toString(), false, DateTime.now(),
                      eventName: ev['name']));
                });
                _scrollToBottom();
              }
            });
          }
        }

        // Also listen to default 'message' event
        _socket!.on('message', (data) {
          if (mounted) {
            setState(() {
              _messages.add(SocketMessage(
                  data.toString(), false, DateTime.now(),
                  eventName: 'message'));
            });
            _scrollToBottom();
          }
        });

        _socket!.connect();
      } catch (e) {
        setState(() {
          _messages.add(SocketMessage(
              '${t['failed_to_connect'] ?? 'Failed to initialize connection'}: $e',
              false,
              DateTime.now()));
        });
      }
    }
  }

  void _sendMessage() {
    final t = ref.read(translationsProvider);
    if (!_isConnected) return;

    final evt = _eventNameController.text.isNotEmpty
        ? _eventNameController.text
        : 'message';

    // Collect args
    List<dynamic> dataToSend = [];
    for (var arg in _args) {
      if (arg['content'].isNotEmpty) {
        if (_argType == 'JSON') {
          try {
            dataToSend.add(jsonDecode(arg['content']));
          } catch (e) {
            dataToSend.add(arg['content']);
          }
        } else {
          dataToSend.add(arg['content']);
        }
      }
    }

    dynamic payload;
    if (dataToSend.isEmpty) {
      return; // Nothing to send
    } else if (dataToSend.length == 1) {
      payload = dataToSend[0];
    } else {
      payload = dataToSend;
    }

    setState(() {
      _messages.add(SocketMessage(jsonEncode(payload), true, DateTime.now(),
          eventName: evt));
    });

    if (_ack) {
      _socket?.emitWithAck(evt, payload, ack: (data) {
        if (mounted) {
          setState(() {
            _messages.add(SocketMessage(
                '${t['ack_received'] ?? 'Ack received'}: $data',
                false,
                DateTime.now(),
                eventName: evt));
          });
          _scrollToBottom();
        }
      });
    } else {
      _socket?.emit(evt, payload);
    }

    _scrollToBottom();
  }

  void _scrollToBottom() {
    Future.delayed(const Duration(milliseconds: 100), () {
      if (_scrollController.hasClients) {
        _scrollController.animateTo(
          _scrollController.position.maxScrollExtent,
          duration: const Duration(milliseconds: 300),
          curve: Curves.easeOut,
        );
      }
    });
  }

