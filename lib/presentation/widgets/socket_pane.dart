  @override
  void initState() {
    super.initState();
    _tabController = TabController(length: 5, vsync: this);
    _urlFocusNode.addListener(() => setState(() {}));
    WidgetsBinding.instance.addPostFrameCallback((_) {
      final req = ref.read(requestProvider);
      if (req.url.isNotEmpty) {
        _urlController.text = req.url;
      }
      if (req.settings.containsKey('socketSettings')) {
        final s = req.settings['socketSettings'];
        if (s['verifyCertificate'] != null)
          _verifyCertificate = s['verifyCertificate'];
        if (s['clientVersion'] != null) _clientVersion = s['clientVersion'];
        if (s['handshakePath'] != null)
          _handshakePathController.text = s['handshakePath'];
        if (s['timeout'] != null)
          _timeoutController.text = s['timeout'].toString();
        if (s['reconnectAttempts'] != null)
          _reconnectAttemptsController.text = s['reconnectAttempts'].toString();
        if (s['reconnectInterval'] != null)
          _reconnectIntervalController.text = s['reconnectInterval'].toString();
      }
      if (req.settings.containsKey('socketEvents')) {
        _events = List<Map<String, dynamic>>.from(req.settings['socketEvents']
            .map((e) => Map<String, dynamic>.from(e)));
      }
      if (req.settings.containsKey('socketArgs')) {
        _args = List<Map<String, dynamic>>.from(req.settings['socketArgs']
            .map((e) => Map<String, dynamic>.from(e)));
        if (_args.isEmpty) {
          _args = [
            {'id': '1', 'name': 'Arg 1', 'content': ''}
          ];
        }
      }
      if (req.settings.containsKey('socketArgType')) {
        _argType = req.settings['socketArgType'];
      }
      if (req.settings.containsKey('socketAck')) {
        _ack = req.settings['socketAck'];
      }
      if (req.settings.containsKey('socketEventName')) {
        _eventNameController.text = req.settings['socketEventName'];
      }
      setState(() {
        _params = List<Map<String, String>>.from(
            req.params.map((e) => Map<String, String>.from(e)));
        _headers = List<Map<String, String>>.from(
            req.headers.map((e) => Map<String, String>.from(e)));
      });
    });
  }

